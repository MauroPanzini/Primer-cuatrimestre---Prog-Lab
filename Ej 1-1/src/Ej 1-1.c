#include <stdio.h>
#include <stdlib.h>

int main(void) {
	int numeroUno;
	int numeroDos;
	int suma;
	printf("Introduzca un número: ");
	scanf("%d" , &numeroUno);
	printf("Introduzca otro número: ");
	scanf("%d" , &numeroDos);
	suma = numeroUno + numeroDos;
	printf("La suma entre %d y %d, da como resultado %d", numeroUno, numeroDos, suma);
	return 0;
}
